package info.bytecraft.annotation;

/**
 * Holds all the data types that are available to be used
 * @author Sabersamus <rcatron10@gmail.com>
 *
 */
public enum DataType {
	/**
	 * Represents an integer
	 */
	INT,
	
	/**
	 * Represents a string
	 */
	STRING,
	
	/**
	 * Represents a boolean
	 */
	BOOLEAN,
	
	/**
	 * Represents a byte
	 */
	BYTE,
	
	/**
	 * Represents a long
	 */
	LONG,
	
	/**
	 * Represents a float
	 */
	FLOAT,
	
	/**
	 * Represents a list
	 */
	LIST,
	
	/**
	 * Represents an array
	 */
	ARRAY,
	
	/**
	 * Represents a class
	 */
	CLASS,
	
	/**
	 * Represents a constructor
	 */
	CONSTRUCTOR,
	
	/**
	 * Represents a method
	 */
	METHOD,
    
    /**
     * Represents a null data type
     */
    NULL,
    
    /**
     * Represents an unknown data type
     */
    UNKNOWN,
    
    /**
     * Represents a field
     */
    FIELD,
    
    /**
     * Represents a hashmap
     */
    HASHMAP,
    
    /**
     * Represents a local variable
     */
    VARIABLE

}
